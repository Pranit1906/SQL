/*

RDBMS
RDBMS stands for Relational Database Management System.

RDBMS is the basis for SQL, and for all modern database systems such as MS SQL Server,
 IBM DB2, Oracle, MySQL, and Microsoft Access.

The data in RDBMS is stored in database objects called tables. A table is a collection of related data entries and 
it consists of columns and rows.

Every table is broken up into smaller entities called fields. The fields in the Customers table consist of 
CustomerID, CustomerName, ContactName, Address, City, PostalCode and Country. A field is a column in a table
that is designed to maintain specific information about every record in the table.

A record, also called a row, is each individual entry that exists in a table. For example, there are 91 records
 in the above Customers table. A record is a horizontal entity in a table.

A column is a vertical entity in a table that contains all information associated with a specific field in a table.

*/

